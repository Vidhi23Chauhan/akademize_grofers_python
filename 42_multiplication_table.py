def table(n):
    for i in range(1,10+1):
        print(f"{n} * {i} = {n*i}")

def main():
    print("======Multiplication Tables======")
    table(2)
    print("-----------")
    table(5)
    print("-----------")
    table(10)
    print("-----------")

main()

